    private boolean validarDatos(){
        if(!(this.nombre.isEmpty() || this.apellidos.isEmpty() || this.dni.isEmpty() || this.correo.isEmpty())){
            if(validarEdad()){
                if(validarDNI()){
                    if(validarCorreo()){
                        return true;
                    }
                }
            }
        } else{
            JOptionPane.showMessageDialog(this, "Los campos no pueden estar vacíos", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }
    
    
    private boolean validarEdad(){
        try {
            this.edad = Integer.valueOf(tf_Edad.getText());
            return true;
        } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "La edad debe ser un número entero", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        return false;
    }
    
    private boolean validarDNI(){
        Pattern patron = Pattern.compile("[0-9]{7,8}[A-Z a-z]");
        Matcher mat = patron.matcher(this.dni);
        if(mat.matches()){
            return true;
        } else{
            JOptionPane.showMessageDialog(this, "El formato de DNI no es correcto", "ERROR", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }
    
    
    private boolean validarCorreo(){
        String regx = "^(.+)@(.+)$";
        Pattern pattern = Pattern.compile(regx);
        //Iterate emails array list  
        Matcher mather = pattern.matcher(this.correo);
 
        if (mather.find() == true) { 
            return true;        
        } else {
            JOptionPane.showMessageDialog(this, "El correo no es válido", "ERROR", JOptionPane.ERROR_MESSAGE);            
            return false;
        }
    }

