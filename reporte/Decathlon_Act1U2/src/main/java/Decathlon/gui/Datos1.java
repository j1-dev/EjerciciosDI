        this.genero = cb_Genero.getSelectedItem().toString();
        this.dni = tf_Dni.getText();
        this.correo = tf_Correo.getText();
        if(validarDatos()){
            try {
                if(this.edad > 0){
                    Datos2 datos2 = new Datos2(this,true);
                    datos2.setVisible(true);
                } else{
                    JOptionPane.showMessageDialog(this, "La edad debe ser un número mayor a 0", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e){
                e.printStackTrace();
            }
        }
